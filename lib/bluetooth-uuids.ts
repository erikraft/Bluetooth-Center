function buildBulkUuidPool(amount: number, seed = 0x1a2b3c4d): string[] {
  const output: string[] = [];
  let state = seed >>> 0;

  const next = () => {
    state ^= state << 13;
    state ^= state >>> 17;
    state ^= state << 5;
    return state >>> 0;
  };

  for (let index = 0; index < amount; index += 1) {
    const a = next().toString(16).padStart(8, "0");
    const b = (next() & 0xffff).toString(16).padStart(4, "0");
    const c = (((next() & 0x0fff) | 0x4000) & 0xffff).toString(16).padStart(4, "0");
    const d = (((next() & 0x3fff) | 0x8000) & 0xffff).toString(16).padStart(4, "0");
    const e = next().toString(16).padStart(8, "0");
    const f = (next() & 0xffff).toString(16).padStart(4, "0");
    output.push(`${a}-${b}-${c}-${d}-${e}${f}`);
  }

  return output;
}

